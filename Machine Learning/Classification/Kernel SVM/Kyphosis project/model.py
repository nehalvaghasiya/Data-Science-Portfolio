df = pd.read_csv('/content/drive/MyDrive/Colab Notebooks/Machine learning/Classification/Kernel SVM/Kyphosis project/kyphosis.csv')

df.head()

"""## EDA


"""

sns.pairplot(df,hue='Kyphosis',palette='Set1')

"""## Train Test Split


"""

from sklearn.model_selection import train_test_split

X = df.drop('Kyphosis',axis=1)
y = df['Kyphosis']

X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.30)

"""#Feature scaling"""

from sklearn.preprocessing import StandardScaler
sc = StandardScaler()
X_train = sc.fit_transform(X_train)
X_test = sc.transform(X_test)

"""## Training the Kernel SVM model on the Training set"""

from sklearn.svm import SVC
model = SVC(kernel = 'rbf', random_state = 0)
model.fit(X_train, y_train)

"""## Predicting a new result"""

y_pred = model.predict(X_test)
print(y_pred)

"""# Evaluation 

"""

from sklearn.metrics import confusion_matrix, classification_report
print(confusion_matrix(y_test,y_pred))

print(classification_report(y_test,y_pred))

#Store the model parameters using Pickle libraries

import pickle
pickle.dump(model, open('kyphosis_kernel_svm_model.pkl','wb'))

